package br.edu.ifrs.canoas.tads.lds.bean;

public class CateterismoReportBean {

	private String internacao;
	
	private String alta;
	
	private String laudo;
	
	private String solicitante;
	
	private String responsavel;
	
	private String observacoes;

	private String nome;
	
	private String genero;
	
	private String nascimento;
	
	private String tiposanguineo;
	
	public String getGenero() {
		return genero;
	}

	public void setGenero(String genero) {
		this.genero = genero;
	}

	public String getNascimento() {
		return nascimento;
	}

	public void setNascimento(String nascimento) {
		this.nascimento = nascimento;
	}

	public String getTiposanguineo() {
		return tiposanguineo;
	}

	public void setTiposanguineo(String tiposanguineo) {
		this.tiposanguineo = tiposanguineo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getInternacao() {
		return internacao;
	}

	public void setInternacao(String internacao) {
		this.internacao = internacao;
	}

	public String getAlta() {
		return alta;
	}

	public void setAlta(String alta) {
		this.alta = alta;
	}

	public String getLaudo() {
		return laudo;
	}

	public void setLaudo(String laudo) {
		this.laudo = laudo;
	}

	public String getSolicitante() {
		return solicitante;
	}

	public void setSolicitante(String solicitante) {
		this.solicitante = solicitante;
	}

	public String getResponsavel() {
		return responsavel;
	}

	public void setResponsavel(String repsonsavel) {
		this.responsavel = repsonsavel;
	}

	public String getObservacoes() {
		return observacoes;
	}

	public void setObservacoes(String observacoes) {
		this.observacoes = observacoes;
	}
	
}
